/**
 * Template for a liveicons React component.
 *
 * Two targets are supported:
 * - "svg"  → <motion.svg> wraps everything (whole-icon animation)
 * - "path" → <svg> wraps children, <motion.path> on target paths
 * - "group" → <svg> wraps children, <motion.g> wraps all paths
 */

import type { AnimationDef, SvgAttrs } from "../types";
import type { Transition } from "motion/react";

interface TemplateOptions {
  componentName: string;
  iconName: string;
  source: string;
  svgChildren: string; // Raw SVG inner content (paths, circles, etc.)
  svgAttrs: SvgAttrs;  // Parsed from the source SVG root element
  animation: AnimationDef;
  /** Relative path to packages/react/src/types — varies by output depth */
  typesPath: string;
}

function serializeVariants(variants: Record<string, unknown>): string {
  return JSON.stringify(variants, null, 2)
    .replace(/"([^"]+)":/g, "$1:") // Remove quotes from keys
    .replace(/"/g, '"'); // Keep string values as-is
}

function serializeTransition(transition: Transition): string {
  // JSON.stringify converts Infinity → null; restore it as the literal Infinity
  return JSON.stringify(transition, (_k, v) => (v === Infinity ? "__INFINITY__" : v), 2)
    .replace(/"([^"]+)":/g, "$1:")
    .replace(/"__INFINITY__"/g, "Infinity");
}

/** Build the SVG color/stroke/fill props block based on icon type */
function buildSvgColorProps(svgAttrs: SvgAttrs): string {
  if (svgAttrs.strokeBased) {
    const linecap = svgAttrs.strokeLinecap ? `\n          strokeLinecap="${svgAttrs.strokeLinecap}"` : "";
    const linejoin = svgAttrs.strokeLinejoin ? `\n          strokeLinejoin="${svgAttrs.strokeLinejoin}"` : "";
    return `fill="none"\n          stroke={color}\n          strokeWidth={strokeWidth}${linecap}${linejoin}`;
  }
  return `fill={color}`;
}

function buildSvgTarget(
  opts: TemplateOptions,
  variantConst: string
): string {
  const { animation, svgChildren, svgAttrs } = opts;
  const transition = serializeTransition(animation.transition);
  const colorProps = buildSvgColorProps(svgAttrs);

  return `<motion.svg
          animate={controls}
          xmlns="http://www.w3.org/2000/svg"
          width={size ?? "100%"}
          height={size ?? "100%"}
          viewBox="${svgAttrs.viewBox}"
          ${colorProps}
          transition={{
            ...${transition},
            duration,
            ...(animate === "loop" ? { repeat: Infinity, repeatType: "loop" as const } : {}),
          }}
          variants={${variantConst}}
        >
          ${svgChildren}
        </motion.svg>`;
}

function buildPathTarget(
  opts: TemplateOptions,
  variantConst: string
): string {
  const { animation, svgChildren, svgAttrs } = opts;
  const targetIndices = animation.pathIndex === "all" ? null : (animation.pathIndex ?? [0]);
  const transition = serializeTransition(animation.transition);
  const colorProps = buildSvgColorProps(svgAttrs);

  // Wrap only targeted paths; others stay as plain <path>
  const wrappedChildren = svgChildren
    .split("\n")
    .map((line, idx) => {
      const isTarget = targetIndices === null || targetIndices.includes(idx);
      if (isTarget && line.trim().startsWith("<path")) {
        return line
          .replace("<path", "<motion.path")
          .replace("/>", `\n            animate={controls}\n            initial="normal"\n            variants={${variantConst}}\n            transition={{ ...${transition}, duration }}\n          />`);
      }
      return line;
    })
    .join("\n");

  return `<svg
          xmlns="http://www.w3.org/2000/svg"
          width={size ?? "100%"}
          height={size ?? "100%"}
          viewBox="${svgAttrs.viewBox}"
          ${colorProps}
        >
          ${wrappedChildren}
        </svg>`;
}

export function reactIconTemplate(opts: TemplateOptions): string {
  const { componentName, iconName, source, animation, svgAttrs, typesPath } = opts;

  const variantConst = `${animation.target.toUpperCase()}_VARIANTS`;
  const variants = serializeVariants(animation.variants as Record<string, unknown>);

  const svgBlock =
    animation.target === "path"
      ? buildPathTarget(opts, variantConst)
      : buildSvgTarget(opts, variantConst);

  // strokeWidth prop is only useful for stroke-based icons; still included in
  // the component signature for API consistency but unused for fill-based icons.
  const strokeWidthNote = svgAttrs.strokeBased ? "" : " // unused for fill-based icons";

  return `"use client";

// Auto-generated by scripts/generate.ts — source: ${source}
// DO NOT edit this file. To modify the animation, edit scripts/animations/${iconName}.ts
// then re-run: pnpm generate --icon ${iconName}

import { motion, useAnimation } from "motion/react";
import type { Variants } from "motion/react";
import { forwardRef, useCallback, useEffect, useImperativeHandle, useRef } from "react";
import type { LiveIconProps, LiveIconHandle } from "${typesPath}";
import { resolveSpeed } from "@liveicons/core";

// Animation variants — defined in scripts/animations/${iconName}.ts
const ${variantConst}: Variants = ${variants};

const ${componentName} = forwardRef<LiveIconHandle, LiveIconProps>(
  (
    {
      animate = "on-hover",
      speed = "normal",
      size,
      color = "currentColor",
      strokeWidth = 2,${strokeWidthNote}
      className,
      onMouseEnter,
      onMouseLeave,
      onClick,
      ...props
    },
    ref
  ) => {
    const controls = useAnimation();
    const isControlledRef = useRef(false);
    const hasPlayedOnceRef = useRef(false);
    const duration = resolveSpeed(speed); // applied to transition below

    useEffect(() => {
      if (animate === "loop") {
        controls.start("animate");
      } else if (animate === "once" && !hasPlayedOnceRef.current) {
        hasPlayedOnceRef.current = true;
        controls.start("animate");
      }
    }, [animate, controls]);

    useImperativeHandle(ref, () => {
      isControlledRef.current = true;
      return {
        startAnimation: () => controls.start("animate"),
        stopAnimation: () => controls.start("normal"),
      };
    });

    const handleMouseEnter = useCallback(
      (e: React.MouseEvent<HTMLDivElement>) => {
        if (!isControlledRef.current && animate === "on-hover") {
          controls.start("animate");
        }
        onMouseEnter?.(e);
      },
      [controls, animate, onMouseEnter]
    );

    const handleMouseLeave = useCallback(
      (e: React.MouseEvent<HTMLDivElement>) => {
        if (!isControlledRef.current && animate === "on-hover") {
          controls.start("normal");
        }
        onMouseLeave?.(e);
      },
      [controls, animate, onMouseLeave]
    );

    const handleClick = useCallback(
      (e: React.MouseEvent<HTMLDivElement>) => {
        if (!isControlledRef.current && animate === "on-click") {
          controls.start("animate");
        }
        onClick?.(e);
      },
      [controls, animate, onClick]
    );

    return (
      <div
        className={className}
        onMouseEnter={handleMouseEnter}
        onMouseLeave={handleMouseLeave}
        onClick={handleClick}
        {...props}
      >
        ${svgBlock}
      </div>
    );
  }
);

${componentName}.displayName = "${componentName}";

export { ${componentName} };
export type { LiveIconHandle as ${componentName}Handle };
`;
}
